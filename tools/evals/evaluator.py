"""Evals — LLM-as-judge output quality evaluation.

Evaluates the output of any SE Toolkit module against four dimensions:
  - Completeness: are all expected fields populated with meaningful content?
  - Accuracy: is the content factually grounded and internally consistent?
  - Actionability: can an SE act on this output in a customer meeting?
  - Hallucination Risk: does the output contain unsupported claims?

This is the eval layer that separates production AI systems from demos.
Without evals, you cannot answer "is this output good enough to show a customer?"
LLM-as-judge is the standard pattern used by OpenAI, Google, and Anthropic
for automated output quality measurement at scale.
"""

from __future__ import annotations

import json

from openai import OpenAI

from tools.config import Config

_EVAL_DIMENSIONS = {
    "completeness": (
        "Does the output contain all expected sections and fields? "
        "Are lists non-empty? Are descriptions substantive (not placeholder text)? "
        "Score 0-10 where 10 = fully complete, 0 = mostly empty."
    ),
    "accuracy": (
        "Is the content internally consistent? Do recommendations align with "
        "the stated challenges? Are claims specific and grounded rather than generic? "
        "Score 0-10 where 10 = fully accurate and consistent, 0 = contradictions or errors."
    ),
    "actionability": (
        "Can a Solutions Engineer use this output directly in a customer meeting? "
        "Are next steps clear? Are recommendations specific to this account? "
        "Score 0-10 where 10 = immediately actionable, 0 = too generic to use."
    ),
    "hallucination_risk": (
        "Does the output contain specific claims (numbers, names, product features) "
        "that appear invented rather than derived from the input? "
        "Score 0-10 where 10 = no hallucination risk, 0 = high risk of fabricated content."
    ),
}

_EVAL_SYSTEM_PROMPT = """You are an expert AI output evaluator.
You evaluate the quality of outputs generated by AI tools for enterprise pre-sales teams.

You score outputs on four dimensions (0-10 each) and provide specific, actionable feedback.
You are strict but fair. Generic outputs that could apply to any company score low on actionability.
You always return valid JSON — no markdown fences, no preamble."""


def evaluate(module: str, output: dict | str, config: Config) -> dict:
    """Evaluate a module output using LLM-as-judge.

    Args:
        module: Name of the toolkit module that produced the output.
        output: The output to evaluate (dict or string).
        config: Shared toolkit config with API key and model.

    Returns:
        Evaluation result with scores, justifications, and overall grade.
    """
    client = OpenAI(api_key=config.openai_api_key, timeout=60.0)

    output_str = json.dumps(output, indent=2) if isinstance(output, dict) else output
    output_preview = output_str[:8000]

    dimensions_desc = "\n".join(
        f'- "{dim}": {desc}' for dim, desc in _EVAL_DIMENSIONS.items()
    )

    user_prompt = f"""Evaluate the following output from the "{module}" module of the Solutions Engineer Toolkit.

Score each dimension 0-10 and provide a one-sentence justification.

Dimensions to score:
{dimensions_desc}

Output to evaluate:
{output_preview}

Return JSON with this exact schema:
{{
  "module": "{module}",
  "scores": {{
    "completeness": 0,
    "accuracy": 0,
    "actionability": 0,
    "hallucination_risk": 0
  }},
  "justifications": {{
    "completeness": "",
    "accuracy": "",
    "actionability": "",
    "hallucination_risk": ""
  }},
  "overall_score": 0,
  "overall_grade": "A | B | C | D | F",
  "top_strength": "",
  "top_improvement": "",
  "production_ready": true
}}

overall_score = average of the four dimension scores (rounded to 1 decimal).
overall_grade: A=9-10, B=7-8, C=5-6, D=3-4, F=0-2.
production_ready = true only if overall_score >= 7.0."""

    response = client.responses.create(
        model=config.model,
        input=[
            {"role": "system", "content": _EVAL_SYSTEM_PROMPT},
            {"role": "user", "content": user_prompt},
        ],
        temperature=0.1,
    )

    content = response.output_text.strip()
    if content.startswith("```"):
        content = content.split("```")[1]
        if content.startswith("json"):
            content = content[4:]

    result = json.loads(content)

    scores = result.get("scores", {})
    if scores and "overall_score" not in result:
        result["overall_score"] = round(
            sum(scores.values()) / len(scores), 1
        )

    return result


def evaluate_pipeline(outputs: dict[str, dict | str], config: Config) -> dict:
    """Evaluate all modules in a pipeline run and return an aggregate report."""
    results = {}
    for module, output in outputs.items():
        results[module] = evaluate(module=module, output=output, config=config)

    scores = [r.get("overall_score", 0) for r in results.values()]
    pipeline_score = round(sum(scores) / len(scores), 1) if scores else 0.0

    return {
        "pipeline_score": pipeline_score,
        "pipeline_grade": _score_to_grade(pipeline_score),
        "production_ready": pipeline_score >= 7.0,
        "module_results": results,
    }


def _score_to_grade(score: float) -> str:
    if score >= 9:
        return "A"
    if score >= 7:
        return "B"
    if score >= 5:
        return "C"
    if score >= 3:
        return "D"
    return "F"
